def format_text_randomly(text):
    text = text.strip()
    if random.random() < 0.4 and text:
        text = text[0].lower() + text[1:]
    if text.endswith('.'):
        if random.random() < 0.5:
            text = text[:-1]
        elif random.random() < 0.1:
            text = text[:-1] + "..."
    else:
        if random.random() < 0.3:
            text += "."
    return text

